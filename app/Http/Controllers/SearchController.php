     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(SearchRequest $request)
    {
        $search = $request['search'];
        $repliced_chars = array(' ', '-', '_', ',','.', '/');
        $search_text = '%'.str_replace($repliced_chars, "%", $search).'%';
        $page=page::where('title', 'like', $search_text)
            ->orWhere('keywords', 'like', $search_text)
            ->orderBy('page_rank','DESC')
            ->paginate(10);
        return view('search.index', compact('page'), compact('search'));
    }

